# Task 5

# Use the type() function to determine the type of each of the following values.

# False
# 1000
# 100.111
# "Hello"
# True
# 100 / 5
# 100 // 5
# Note: The division (‘/’) operator always returns a float-type value, even when the operands are integer-type values.

print(type(False))
print(type(1000))
print(type(100.111))
print(type("Hello"))
print(type(True))
print(type(100 / 5))
print(type(100 // 5))

# Output:
#
# >>> type(False)
# <class 'bool'>
# >>> type(1000)
# <class 'int'>
# >>> type(100.111)
# <class 'float'>
# >>> type("Hello")
# <class 'str'>
# >>> type(True)
# <class 'bool'>
# >>> type(100 / 5)
# <class 'float'>
# >>> type(100 // 5)
# <class 'int'>