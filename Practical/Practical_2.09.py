# Write a program that prompts the user to input two numeric values.
# Once the values have been input display the product of these values,
# using the multiply (*) operator.

value_1 = int(input("Please enter a number: "))
value_2 = int(input("Please enter another number: "))
print(f"The first number was {value_1}\nThe second value was {value_2}")
print(f"The two numbers {value_1} and {value_2} product are {value_1*value_2}.")

# Output:
#
# Please enter a number: 12
# Please enter another number: 10
# The first number was 12
# The second value was 10
# The two numbers 12 and 10 product are 120.
